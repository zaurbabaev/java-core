package lesson_3.logical_operators;

public class Example7 {
    public static void main(String[] args) {
        char a = 'a';
        int b = 10;
        System.out.println(a + b); //107

        a = 100;
        System.out.println(a + b); // 110


        boolean b1 = false;
        boolean b2 = b1 == true;
        System.out.println(b2); // false

    }
}

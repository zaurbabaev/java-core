package lesson_3.logical_operators;

/*
 && , || ,  & , | , ^ , !
*/
public class Example1 {
    public static void main(String[] args) {

        boolean x = true;
        boolean y = true;
        boolean z = false;

        System.out.println(x && y && z);

        System.out.println(x || y || z);

        System.out.println(!x);
        System.out.println(!z);





    }
}
